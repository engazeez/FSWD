package ConstructorMethods;

public class Calculate {
	
	public static int addingTwoNumers(int num1, int num2) {
		// adding two numbers
		return num1 + num2;
	}
	
	public static float circleArea(float radius) {
		// calculating area of circle
		float pie = (float) 3.14;
		return radius * pie;
	}
	
	public static int rectangularArea(int lenght, int breadth) {
		// calculating area of rectangular
		return lenght * breadth;
	}
}
